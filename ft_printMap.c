#include "fillit.h"

void	ft_printMap(char **map)
{
	int i;
	int j;

	i = -1;
	j = -1;
	while (map[++j])
	{
		while (map[j][++i])
		{
			ft_putchar(map[j][i]);
		}
		i = -1;
		ft_putchar('\n');
	}
}
