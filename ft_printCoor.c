#include "fillit.h"

void	ft_printCoor(int **coor)
{
	int i;
	int j;

	i = 0;
	while (i < 2)
	{
		j = 0;
		while (j < 4)
		{
			ft_putnbr(coor[i][j]);
			j++;
		}
		ft_putchar('\n');
		i++;
	}
}
